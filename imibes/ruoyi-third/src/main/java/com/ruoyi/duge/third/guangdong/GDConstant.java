package com.ruoyi.duge.third.guangdong;

public class GDConstant {

    public static String mappingPlateColor(String colorChs){
        switch (colorChs){
            case "蓝":
                return "1";
            case "黄":
                return "2";
            case "黑":
                return "3";
            case "白":
                return "4";
            case "绿":
                return "5";
            default:
                return "9";
        }
    }

    public static String mappingVehicleColor(String colorChs){
        switch (colorChs){
            case "白":
                return "A";
            case "灰":
                return "B";
            case "黄":
                return "C";
            case "粉":
                return "D";
            case "红":
                return "E";
            case "紫":
                return "F";
            case "绿":
                return "G";
            case "蓝":
                return "H";
            case "棕":
                return "I";
            case "黑":
                return "J";
            default:
                return "Z";
        }
    }
}
