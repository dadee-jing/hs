package com.ruoyi.yaogan.domain;

import com.ruoyi.common.base.BaseEntity;
import org.apache.commons.lang3.builder.ToStringBuilder;
import org.apache.commons.lang3.builder.ToStringStyle;

import java.math.BigDecimal;
import java.util.Date;

/**
 * 机动车轨迹表 rs_vehicle_trajectory
 * 
 * @author ruoyi
 * @date 2019-04-08
 */
public class VehicleTrajectory extends BaseEntity
{
	private static final long serialVersionUID = 1L;
	
	/** 轨迹信息编号 */
	private String gJXXBH;
	/** 点位编号 */
	private String dWBH;
	/** 监测点位日志号 */
	private String jCDWRZH;
	/** 车道编号 */
	private String cDBH;
	/** 通过日期 */
	private Date tGSJ;
	/** 车辆速度 */
	private BigDecimal cLSD;
	/** 号牌号码 */
	private String hPHM;
	/** 车身颜色 */
	private String cSYS;
	/** 号牌种类 */
	private String hPZL;
	/** 识别置信度 */
	private Integer sBZXD;

	public void setGJXXBH(String gJXXBH) 
	{
		this.gJXXBH = gJXXBH;
	}

	public String getGJXXBH() 
	{
		return gJXXBH;
	}
	public void setDWBH(String dWBH) 
	{
		this.dWBH = dWBH;
	}

	public String getDWBH() 
	{
		return dWBH;
	}
	public void setJCDWRZH(String jCDWRZH) 
	{
		this.jCDWRZH = jCDWRZH;
	}

	public String getJCDWRZH() 
	{
		return jCDWRZH;
	}
	public void setCDBH(String cDBH) 
	{
		this.cDBH = cDBH;
	}

	public String getCDBH() 
	{
		return cDBH;
	}
	public void setTGSJ(Date tGSJ) 
	{
		this.tGSJ = tGSJ;
	}

	public Date getTGSJ() 
	{
		return tGSJ;
	}
	public void setCLSD(BigDecimal cLSD) 
	{
		this.cLSD = cLSD;
	}

	public BigDecimal getCLSD() 
	{
		return cLSD;
	}
	public void setHPHM(String hPHM) 
	{
		this.hPHM = hPHM;
	}

	public String getHPHM() 
	{
		return hPHM;
	}
	public void setCSYS(String cSYS) 
	{
		this.cSYS = cSYS;
	}

	public String getCSYS() 
	{
		return cSYS;
	}
	public void setHPZL(String hPZL) 
	{
		this.hPZL = hPZL;
	}

	public String getHPZL() 
	{
		return hPZL;
	}
	public void setSBZXD(Integer sBZXD) 
	{
		this.sBZXD = sBZXD;
	}

	public Integer getSBZXD() 
	{
		return sBZXD;
	}

    public String toString() {
        return new ToStringBuilder(this,ToStringStyle.MULTI_LINE_STYLE)
            .append("gJXXBH", getGJXXBH())
            .append("dWBH", getDWBH())
            .append("jCDWRZH", getJCDWRZH())
            .append("cDBH", getCDBH())
            .append("tGSJ", getTGSJ())
            .append("cLSD", getCLSD())
            .append("hPHM", getHPHM())
            .append("cSYS", getCSYS())
            .append("hPZL", getHPZL())
            .append("sBZXD", getSBZXD())
            .toString();
    }
}
