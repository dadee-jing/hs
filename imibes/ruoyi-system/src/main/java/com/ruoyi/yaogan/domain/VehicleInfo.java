package com.ruoyi.yaogan.domain;

import com.ruoyi.common.base.BaseEntity;
import org.apache.commons.lang3.builder.ToStringBuilder;
import org.apache.commons.lang3.builder.ToStringStyle;

import java.util.Date;

/**
 * 车辆数据表 rs_vehicle_info
 * 
 * @author ruoyi
 * @date 2019-04-08
 */
public class VehicleInfo extends BaseEntity
{
	private static final long serialVersionUID = 1L;
	
	/** ID */
	private Integer iD;
	/** 行政区划代码 */
	private String xZQHDM;
	/** 号牌号码 */
	private String hPHM;
	/** 车牌颜色 */
	private String hPYS;
	/** 号牌种类 */
	private String hPZL;
	/** 车辆型号 */
	private String cLXH;
	/** 生产企业 */
	private String sCQY;
	/** 燃料种类 */
	private String rLZL;
	/** 使用性质 */
	private String sYXZ;
	/** 初次登记日期 */
	private Date cCDJRQ;
	/** 车辆识别代号 */
	private String cLSBDH;
	/** 排放标准阶段 */
	private String pFBZJD;
	/** 上次环保定期检验日期 */
	private Date sCJYRQ;
	/** 上次环保定期检验机构 */
	private String sCJYJG;

	public void setID(Integer iD) 
	{
		this.iD = iD;
	}

	public Integer getID() 
	{
		return iD;
	}
	public void setXZQHDM(String xZQHDM) 
	{
		this.xZQHDM = xZQHDM;
	}

	public String getXZQHDM() 
	{
		return xZQHDM;
	}
	public void setHPHM(String hPHM) 
	{
		this.hPHM = hPHM;
	}

	public String getHPHM() 
	{
		return hPHM;
	}
	public void setHPYS(String hPYS) 
	{
		this.hPYS = hPYS;
	}

	public String getHPYS() 
	{
		return hPYS;
	}
	public void setHPZL(String hPZL) 
	{
		this.hPZL = hPZL;
	}

	public String getHPZL() 
	{
		return hPZL;
	}
	public void setCLXH(String cLXH) 
	{
		this.cLXH = cLXH;
	}

	public String getCLXH() 
	{
		return cLXH;
	}
	public void setSCQY(String sCQY) 
	{
		this.sCQY = sCQY;
	}

	public String getSCQY() 
	{
		return sCQY;
	}
	public void setRLZL(String rLZL) 
	{
		this.rLZL = rLZL;
	}

	public String getRLZL() 
	{
		return rLZL;
	}
	public void setSYXZ(String sYXZ) 
	{
		this.sYXZ = sYXZ;
	}

	public String getSYXZ() 
	{
		return sYXZ;
	}
	public void setCCDJRQ(Date cCDJRQ) 
	{
		this.cCDJRQ = cCDJRQ;
	}

	public Date getCCDJRQ() 
	{
		return cCDJRQ;
	}
	public void setCLSBDH(String cLSBDH) 
	{
		this.cLSBDH = cLSBDH;
	}

	public String getCLSBDH() 
	{
		return cLSBDH;
	}
	public void setPFBZJD(String pFBZJD) 
	{
		this.pFBZJD = pFBZJD;
	}

	public String getPFBZJD() 
	{
		return pFBZJD;
	}
	public void setSCJYRQ(Date sCJYRQ) 
	{
		this.sCJYRQ = sCJYRQ;
	}

	public Date getSCJYRQ() 
	{
		return sCJYRQ;
	}
	public void setSCJYJG(String sCJYJG) 
	{
		this.sCJYJG = sCJYJG;
	}

	public String getSCJYJG() 
	{
		return sCJYJG;
	}

    public String toString() {
        return new ToStringBuilder(this,ToStringStyle.MULTI_LINE_STYLE)
            .append("iD", getID())
            .append("xZQHDM", getXZQHDM())
            .append("hPHM", getHPHM())
            .append("hPYS", getHPYS())
            .append("hPZL", getHPZL())
            .append("cLXH", getCLXH())
            .append("sCQY", getSCQY())
            .append("rLZL", getRLZL())
            .append("sYXZ", getSYXZ())
            .append("cCDJRQ", getCCDJRQ())
            .append("cLSBDH", getCLSBDH())
            .append("pFBZJD", getPFBZJD())
            .append("sCJYRQ", getSCJYRQ())
            .append("sCJYJG", getSCJYJG())
            .toString();
    }
}
